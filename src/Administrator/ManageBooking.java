    public void generateTable() {
        String[] columns = {"Customer", "Contact No.", "Hall ID", "Event", "Reservation Date", "Start", "End", "Participant No."};
        DefaultTableModel model = new DefaultTableModel(columns, 0);

        file = new FileOperation();
        file.setFile("resources/database/reservation.txt");
        file.checkFilePath();
        file.retrieveData();
        ArrayList<String> reservationRecords = file.getRetrievedData();

        // Create a SimpleDateFormat to parse dates from the records
        SimpleDateFormat formatter = new SimpleDateFormat("dd MMMM yyyy");

        // Create a list to store the records along with their parsed dates
        ArrayList<String[]> recordsWithDates = new ArrayList<>();
        
        // Create a Set to store unique customer names for the combo box
        Set<String> uniqueCustomers = new HashSet<>();

        try {
            // Iterate through the records and collect them
            for (String record : reservationRecords) {
                String[] row = record.split(",");

                String email = row[9];
                String contactNo = row[10]; 
                String ID = row[1];
                String Date = row[2];
                String startingTime = row[3];
                String endingTime = row[5];
                String event = row[6];
                String participantNum = row[8];

                // Parse the date and store it along with the rest of the data
                Date reservationDate = formatter.parse(Date);
                recordsWithDates.add(new String[]{email, contactNo, ID, Date, startingTime, endingTime, event, participantNum});
                
                // Add the customer email to the uniqueCustomers set for the combo box
                uniqueCustomers.add(email);
            }

            // Sort the records by date (oldest to newest)
            recordsWithDates.sort((a, b) -> {
                try {
                    return formatter.parse(a[3]).compareTo(formatter.parse(b[3]));
                } catch (ParseException e) {
                    e.printStackTrace();
                }
                return 0;
            });

            // Add the sorted records to the table
            for (String[] record : recordsWithDates) {
                model.addRow(new Object[]{record[0], record[1], record[2], record[6], record[3], record[4], record[5], record[7]});
            }

        } catch (ParseException e) {
            e.printStackTrace();
        }

        // Set the model to the table
        bookingTable.setModel(model);
        choiceInput.setSelectedIndex(0);
        
        customerComboBox.removeAllItems(); // Clear any previous items
        customerComboBox.addItem("Select a Customer"); // Add default prompt
        for (String customer : uniqueCustomers) {
            customerComboBox.addItem(customer); // Add each customer to the combo box
        }
        
        // Set column widths
        int[] columnWidths = {150, 90, 50, 170, 120, 35, 35, 33}; // Total = 683

        for (int i = 0; i < columns.length; i++) {
            bookingTable.getColumnModel().getColumn(i).setPreferredWidth(columnWidths[i]);
        }
    }

