    private void printButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_printButtonMouseClicked
        String userChoice = (String) choiceInput.getSelectedItem();
        String headerText = "Summary of ";

        // Adjust the header text based on the user's selection
        if (userChoice.equals("All Reservation")) {
            headerText += "Overall Booking Reservation";
        } else if (userChoice.equals("Upcoming Reservation")) {
            headerText += "Upcoming Booking Reservation";
        } else if (userChoice.equals("Past Reservation")) {
            headerText += "Past Booking Reservation";
        }

        // Create header and footer with the updated header text
        MessageFormat header = new MessageFormat(headerText);
        MessageFormat footer = new MessageFormat("Page {0, number, integer}");

        try {
            // Print the booking table with the updated header and footer
            bookingTable.print(JTable.PrintMode.FIT_WIDTH, header, footer);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_printButtonMouseClicked

