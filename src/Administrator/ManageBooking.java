    private void filterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterButtonActionPerformed
        DefaultTableModel table = (DefaultTableModel) bookingTable.getModel();
        table.setRowCount(0); // Clear the table

        String userChoice = (String) choiceInput.getSelectedItem();
        String selectedCustomer = (String) customerComboBox.getSelectedItem();
        SimpleDateFormat formatter = new SimpleDateFormat("dd MMMM yyyy");
        Date todayDate = new Date(); // Get today's date

        // Check if the user selected "All Reservation" and "Select a Customer"
        if (userChoice.equals("All Reservation") && selectedCustomer.equals("Select a Customer")) {
            generateTable(); // Load all reservations without filtering
            return; // Exit the method as no further filtering is needed
        }

        ArrayList<String[]> filteredRecords = new ArrayList<>();

        try {
            // Retrieve all records from the file
            ArrayList<String> records = file.getRetrievedData();

            // Iterate through the records to filter them based on the user's choice
            for (String record : records) {
                String[] row = record.split(",");

                String customer = row[9];               // Customer
                String contactNo = row[10];             // Contact No.
                String currentID = row[1];              // Hall ID
                String currentDate = row[2];            // Reservation Date
                String currentStartingTime = row[3];    // Starting Time
                String currentEndingTime = row[5];      // Ending Time
                String currentEvent = row[6];           // Event
                String currentParticipantNum = row[8];  // Participant Number

                // Parse the reservation date
                Date reservationDate = formatter.parse(currentDate);
                
               boolean addRecord = false;
            
                // Check if userChoice is "All Reservation"
                if (userChoice.equals("All Reservation")) {
                    // Include all records related to the selected customer
                    if (selectedCustomer.equals("Select a Customer") || customer.equals(selectedCustomer)) {
                        addRecord = true;
                    }
                } else {
                    // Filter based on reservation type (upcoming or past) and selected customer
                    if (userChoice.equals("Upcoming Reservation") && reservationDate.after(todayDate)) {
                        addRecord = true;
                    } else if (userChoice.equals("Past Reservation") && reservationDate.before(todayDate)) {
                        addRecord = true;
                    }

                    // Check if the record also matches the selected customer if a customer is chosen
                    if (!selectedCustomer.equals("Select a Customer") && !customer.equals(selectedCustomer)) {
                        addRecord = false;
                    }
                }

                if (addRecord) {
                    filteredRecords.add(new String[]{customer, contactNo, currentID, currentEvent, currentDate, currentStartingTime, currentEndingTime, currentParticipantNum});
                }
            }

            // Sort the filtered records by reservation date (oldest to newest)
            filteredRecords.sort((a, b) -> {
                try {
                    return formatter.parse(a[4]).compareTo(formatter.parse(b[4])); // Compare by reservation date (index 4)
                } catch (ParseException e) {
                    e.printStackTrace();
                }
                return 0;
            });

            // Add the sorted filtered records to the table
            for (String[] record : filteredRecords) {
                table.addRow(new Object[]{record[0], record[1], record[2], record[3], record[4], record[5], record[6], record[7]});
            }

        } catch (ParseException ex) {
            Logger.getLogger(ManageBooking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_filterButtonActionPerformed

