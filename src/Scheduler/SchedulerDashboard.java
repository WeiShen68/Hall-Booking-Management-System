    private void FilterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FilterButtonActionPerformed
        DefaultTableModel table = (DefaultTableModel) hallTable.getModel();
        table.setRowCount(0); // Clear existing rows

        // Get user search input and hall type
        String userSearch = searchField.getText().trim();
        String hallType = String.valueOf(hallTypeDropdown.getSelectedItem()).trim();
        boolean maintenanceCheckSelected = maintanenceCheck.isSelected();

        // Set file path and retrieve data
        FileOperation file = new FileOperation();
        file.setFile("resources/database/hall.txt");
        file.checkFilePath();
        file.retrieveData();

        ArrayList<String> hallRecords = file.getRetrievedData();
        boolean matchingResult = false;
        ArrayList<String> filteredHallRecord = new ArrayList<>();

        // Loop through hall records
        for (String record : hallRecords) {
            String[] row = record.split(",");

            // Assuming the row array structure is [id, type, price, capacity, ..., maintenanceStatus]
            String id = row[0];
            String type = row[1];
            String price = row[2];
            String capacity = row[3];
            boolean maintenanceStatus = Boolean.parseBoolean(row[7]); // Ensure maintenanceStatus is treated as boolean

            // Check if the hall type and search term match
            boolean typeMatches = hallType.equalsIgnoreCase("all") || type.equalsIgnoreCase(hallType);
            boolean searchMatches = userSearch.isEmpty() || record.toLowerCase().contains(userSearch.toLowerCase());
            boolean maintenanceMatches = !maintenanceCheckSelected || maintenanceStatus; 

            if (typeMatches && searchMatches && maintenanceMatches) {
                matchingResult = true;
                filteredHallRecord.add(record);

                // Add matching record to the table
                table.addRow(new Object[]{id, type, price, capacity});
            }
        }

        // Check if any matching records were found
        if (matchingResult) {
            JOptionPane.showMessageDialog(rootPane, "Record(s) Found!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Sorry, no records found.", "Record Not Found", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_FilterButtonActionPerformed

