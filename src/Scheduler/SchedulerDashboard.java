    public void generateTable() {
        String columns[] = {"Hall ID", "Hall Type", "Price per Hour", "Hall Capacity"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        
        FileOperation file = new FileOperation();
        file.setFile("resources/database/hall.txt");
        file.checkFilePath();
        file.retrieveData();
        
        ArrayList<String> hallRecords = file.getRetrievedData();
        for(String record : hallRecords) {
            String[] details = record.split(",");
            String ID = details[0];
            String hallType = details[1];
            double hallPrice = Double.parseDouble(details[2]);
            int hallCapacity = Integer.parseInt(details[3]);
            
            model.addRow(new Object[]{ID, hallType, hallPrice, hallCapacity});
        }
        
        hallTable.setModel(model);
    }

