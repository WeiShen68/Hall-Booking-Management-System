    public void generateTable() {
    String columns[] = {"Reservation ID", "Hall ID", "Reservation Date", "Starting Time", "Duration", "Ending Time", "Total Price"};
    DefaultTableModel model = new DefaultTableModel(columns, 0);

    FileOperation file = new FileOperation();
    file.setFile("resources/database/reservation.txt");
    file.checkFilePath();
    file.retrieveData();

    ArrayList<String> reservationRecords = file.getRetrievedData();
    
    SimpleDateFormat inputFormat = new SimpleDateFormat("dd MMMM yyyy");
    SimpleDateFormat outputDateFormat = new SimpleDateFormat("dd/MM/yyyy");
    
      Map<String, Double> hallPrices = Map.of(
        "H1", 300.00,
        "H2", 100.00,
        "H3", 50.00
    );

    for (String record : reservationRecords) {
        String[] details = record.split(",");
        String reservationID = details[0];
        String hallID = details[1];
        String reservationDateStr = details[2];
        String startingTime = details[3];
        String durationStr = details[4];

        String formattedReservationDate = "";
        try {
            Date date = inputFormat.parse(reservationDateStr);
            formattedReservationDate = outputDateFormat.format(date);
        } catch (ParseException e) {
            e.printStackTrace();
            formattedReservationDate = reservationDateStr; 
        }

         double startingTimeDouble = Double.parseDouble(startingTime);
        double durationDouble = Double.parseDouble(durationStr);
        double endingTimeDouble = startingTimeDouble + durationDouble;
        String endingTime = String.format("%.1f", endingTimeDouble);
        
        // Calculate total price
        double pricePerHour = hallPrices.getOrDefault(hallID, 0.0);
        double totalPrice = durationDouble * pricePerHour;
        
        model.addRow(new Object[]{
            reservationID, 
            hallID, 
            formattedReservationDate, 
            startingTime, 
            durationStr, 
            endingTime, 
            String.format("%.2f", totalPrice) // Total Price
        });
    }

    // Set the model to the table
    SalesTable.setModel(model);
    }
    
    private String formatTime(double time) {
        int hours = (int) time;
        int minutes = (int) ((time - hours) * 60);
        return String.format("%02d%02d", hours, minutes);
    }

