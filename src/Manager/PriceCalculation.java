    private void calculateWeeklyTotals() {
        DefaultTableModel model = (DefaultTableModel) SalesTable.getModel();
        int rowCount = model.getRowCount();

        Map<String, Double> weeklyTotals = new HashMap<>();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");

        for (int i = 0; i < rowCount; i++) {
            String reservationDateStr = (String) model.getValueAt(i, 2); // Column index 2 for reservation date
            String totalPriceStr = (String) model.getValueAt(i, 6); // Column index 6 for total price

            double totalPrice;
            try {
                totalPrice = Double.parseDouble(totalPriceStr);
            } catch (NumberFormatException e) {
                e.printStackTrace();
                continue; 
            }

            try {
                Date reservationDate = dateFormat.parse(reservationDateStr);
                Calendar cal = Calendar.getInstance();
                cal.setTime(reservationDate);

                int weekOfMonth = cal.get(Calendar.WEEK_OF_MONTH);
                int month = cal.get(Calendar.MONTH) + 1; 
                String monthName = new SimpleDateFormat("MMMM").format(reservationDate);
                String weekKey = monthName + " Week " + weekOfMonth;

                weeklyTotals.put(weekKey, weeklyTotals.getOrDefault(weekKey, 0.0) + totalPrice);

            } catch (ParseException e) {
                e.printStackTrace();
            }
        }

        StringBuilder result = new StringBuilder("Weekly Totals:\n");
        for (Map.Entry<String, Double> entry : weeklyTotals.entrySet()) {
            result.append(entry.getKey()).append(": RM ").append(entry.getValue()).append("\n");
        }

        JOptionPane.showMessageDialog(null, result.toString());
    }

