    public void setEventID() {
        FileOperation file = new FileOperation();
        file.setFile("resources/database/reservation.txt");
        file.checkFilePath();
        file.retrieveData();
        
        ArrayList<String> records = file.getRetrievedData();
        for(String data : records) {
            String[] row = data.split(",");
            
            String reservationID = row[0];
            String emailFromFile = row[9];
            
            if(emailFromFile.equals(getEmail())) {
                eventBox.addItem(reservationID);
            }
        }
    }
    
