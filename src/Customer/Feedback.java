    class InputOperation {
        protected String fullname;
        protected String chosenEvent;
        protected String userMessage;
        protected boolean validatedInput = false;
        
        public void setUserInput() {
            fullname = nameField.getText().trim();
            chosenEvent = (String) eventBox.getSelectedItem();
            userMessage = messageField.getText().trim();
        }
        
        public void validateUserInput() {
            if(fullname.isEmpty() || chosenEvent == null || userMessage.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please complete all required fields before sending feedback!", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            } else {
                validatedInput = true;
            }
        }
        
        public void sendFeedback() {
            if(validatedInput) {
                FileOperation file = new FileOperation();
                file.setFile("resources/database/customerFeedback.txt");
                file.retrieveData();
                file.setSplitElement("F");
                file.IdOperation();
                int newID = file.getIdNum();
                String newFeedbackID = "F" + newID;
                
                System.out.println(newFeedbackID + "," + chosenEvent + "," + getEmail() + "," + fullname + "," + userMessage);
                
                FeedbackAndMessage feedback = new FeedbackAndMessage();
                FeedbackAndMessage.Customer customerFeedback = feedback.new Customer(
                    newFeedbackID,
                    chosenEvent, 
                    getEmail(),
                    fullname,
                    userMessage
                );
                
                String feedbackStatus = customerFeedback.getCustomerFeedback();
                
                if(feedbackStatus.equals("sent")) {
                    JOptionPane.showMessageDialog(null, "Feedback has been send to the manager successfully!", "Feedback Sent", JOptionPane.INFORMATION_MESSAGE);
                    customerFeedback.provideFeedback();
                } else {
                    JOptionPane.showMessageDialog(null, "Feedback cancelled!", "Action Paused", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }
    

    
    

