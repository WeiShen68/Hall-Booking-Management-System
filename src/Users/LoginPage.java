    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        accountVerification login = new accountVerification();
        login.readAndSetUserInput();
        login.inputVerification();
        
        if(login.status.equals("blocked")) {
            JOptionPane.showMessageDialog(null, "Your account has been blocked from the system!", "Failed to Login", JOptionPane.ERROR_MESSAGE);
        } else {
            if(login.proceed) {
                if(login.user.getRole().equals("customer")) {
                    Customer user = new Customer();
                    user.navigate(login.user.getName(), login.user.getEmail());
                    this.dispose();
                } 

                if(login.user.getRole().equals("scheduler")) {
                    Scheduler user = new Scheduler();
                    user.navigate(login.user.getName(), login.user.getEmail());
                    this.dispose();
                }

                if(login.user.getRole().equals("manager")) {
                    Manager user = new Manager();
                    user.navigate(login.user.getName(), login.user.getEmail());
                    this.dispose();
                }
                
                if(login.user.getRole().equals("administrator")) {
                    Admin user = new Admin();
                    user.navigate(login.user.getName(), login.user.getEmail());
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please input correct email and password!", "Invalid Credentials", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_loginButtonActionPerformed

