    class accountVerification {
        private String email;
        private String password;
        private String role;
        protected boolean proceed = false;
        protected String status = "active";
        protected boolean successLogin = false;
        User user = new User();
        
        public void readAndSetUserInput() {
            email = emailField.getText().toLowerCase().trim();
            password = passwordField.getText().trim();
            role = (String)roleField.getSelectedItem();
            
            user.setEmail(email);
            user.setPassword(password);
            user.setRole(role);
        }
        
        public void inputVerification() {
            if (email.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please complete all required fields", "Empty Input Found", JOptionPane.ERROR_MESSAGE);
            } else {
                proceed = LoginProcess();
            }
        }
        
        public boolean LoginProcess() {
            FileOperation file = new FileOperation();
            file.setFile("resources/database/users.txt");
            file.checkFilePath();
            file.retrieveData();
            
            ArrayList<String> records = file.getRetrievedData();
            for(String row : records) {
                String[] line = row.split(",");
                
                String emailFromFile = line[0];
                String usernameFromFile = line[1];
                String passFromFile = line[3];
                String roleFromFile = line[4];
                String accountStatus = line[7];
                

                if(emailFromFile.equals(email) && passFromFile.equals(password) && roleFromFile.equals(role)) {
                    user.setName(usernameFromFile);
                    successLogin = true;
                    status = accountStatus;
                } 
                
            }
            return successLogin;
        }
    }

